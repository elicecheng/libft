#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*res;

	res = NULL;
	while (*s)
	{
		if (*s == (c % 128))
		{
			res = (char *)s;
		}
		s++;
	}
	if ((c % 128) == 0)
	{
		return ((char *)s);
	}
	else
	{
		return (res);
	}
}
